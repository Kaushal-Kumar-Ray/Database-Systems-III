CREATE DATABASE e_banking;
USE e_banking;

-- Customer Table
CREATE TABLE IF NOT EXISTS customer (
    cust_id INT PRIMARY KEY AUTO_INCREMENT,
    cust_name VARCHAR(100) NOT NULL,
    cust_address VARCHAR(255),
    cust_mobile VARCHAR(20) CHECK (cust_mobile REGEXP '^[0-9]{10,15}$')
);

-- Account Table
CREATE TABLE IF NOT EXISTS account (
    acct_id INT PRIMARY KEY AUTO_INCREMENT,
    acct_number VARCHAR(20) UNIQUE,
    acct_balance DECIMAL(15,2) NOT NULL DEFAULT 0.00,
    acct_type VARCHAR(50),
    cust_id INT NOT NULL,
    FOREIGN KEY (cust_id) REFERENCES customer(cust_id)

);

-- Transactions Table
CREATE TABLE IF NOT EXISTS transactions (
    txn_id INT PRIMARY KEY AUTO_INCREMENT,
    txn_type ENUM('Credit', 'Debit') NOT NULL,
    txn_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    txn_status ENUM('Pending', 'Completed', 'Failed') NOT NULL DEFAULT 'Pending',
    acct_id INT NOT NULL,
    FOREIGN KEY (acct_id) REFERENCES account(acct_id)
	
);

-- KYC Table
CREATE TABLE IF NOT EXISTS kyc (
    kyc_id INT PRIMARY KEY AUTO_INCREMENT,
    kyc_type ENUM('Aadhar', 'Voter ID') NOT NULL,
    kyc_number VARCHAR(25),
    kyc_status ENUM('Pending', 'Done', 'Failed') NOT NULL DEFAULT 'Pending',
    cust_id INT NOT NULL,
    FOREIGN KEY (cust_id) REFERENCES customer(cust_id)
        
);


